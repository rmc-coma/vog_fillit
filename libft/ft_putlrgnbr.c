#include "libft.h"

void	ft_putlrgnbr(long long n)
{
	if (n == -9223372036854775807)
	{
		ft_putstr("-9223372036854775807");
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n < 10)
		{
			ft_putchar(n + '0');
		}
		else
		{
			ft_putlrgnbr(n / 10);
			ft_putchar(n % 10 + '0');
		}
	}
}
